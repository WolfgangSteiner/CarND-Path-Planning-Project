void TLowpassFilter::Init(const Eigen::VectorXd& aX)
{
  mX = aX;
}


//----------------------------------------------------------------------------------------------

Eigen::VectorXd TLowpassFilter::Update(const Eigen::VectorXd& aX)
{
  mX = mFilterConstant * aX + mX * (1.0 - mFilterConstant);
  return mX;
}


